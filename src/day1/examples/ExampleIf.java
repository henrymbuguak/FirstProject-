package day1.examples;

public class ExampleIf {

	public static void main(String[] args) {
		int x = 30;
		int y = 30;
		
		if (x < y) {
			System.out.println("X is les than y.");
		}
		
		if (x == y) {
			System.out.println("X is equal to Y.");
		}
		
		if (x > y) {
			System.out.println("X is greater than Y.");
		}

	}

}
